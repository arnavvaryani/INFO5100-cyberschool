    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCourse.getModel();
        model.setRowCount(0);

        for (Course course1 : courses.getCourses()) {
            model.addRow(new Object[]{
                course1,
                course1.getCode(),
                course1.getCredits(),
                course1.getDesc(),
                course1.getGrade(),
                course1.getInstructor(),
                course1.getLanguage(),
                course1.getName(),
                course1.getReview(),
                course1.getSchedule(),});
        }
    }

