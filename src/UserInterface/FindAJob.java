    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblJobs.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to apply.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
        Job selectedJob = (Job) model.getValueAt(selectedRowIndex, 0);

        selectedJob.getAppliedStudents().add(s);
        JOptionPane.showMessageDialog(this, "Applied successfully.");
    }//GEN-LAST:event_btnApplyActionPerformed

    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setCurrentDirectory(new File("user.dir"));
        
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Document Files", "pdf", "doc", "docx");
        fc.addChoosableFileFilter(filter);
        
        int res = fc.showSaveDialog(null);
        
        if(res == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            String path = file.getAbsolutePath();
            txtResume.setText(path);
            JOptionPane.showMessageDialog(this, "Resume uploaded successfully.");
            btnApply.setEnabled(true);
        }
    }//GEN-LAST:event_btnUploadActionPerformed


