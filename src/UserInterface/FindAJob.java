    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
        model.setRowCount(0);
        for (Employer e :m.getEmployers().getEmployers()) {
            for (Job j: e.getJobOfferings()) {
                model.addRow(new Object[] {
                    j,
                    j.getDescription(),
                    j.getType(),
                    j.getPayPerHour()
                });
            }
        }
    }
    

