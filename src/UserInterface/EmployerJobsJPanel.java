    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblJobs.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
        Job selectedJob = (Job) model.getValueAt(selectedRowIndex, 0);
        txtName.setText(selectedJob.getName());
        txtDescription.setText(selectedJob.getDescription());
        txtPay.setText(String.valueOf(selectedJob.getPayPerHour()));
        txtType.setText(selectedJob.getType());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblJobs.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
        Job selectedJob = (Job) model.getValueAt(selectedRowIndex, 0);
        e.getJobOfferings().remove(selectedJob);
        JOptionPane.showMessageDialog(this, "Job deleted successfully.");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String name = txtType.getText();
        String descr = txtType.getText();
        String type = txtType.getText();
        try {
            float pay = Float.parseFloat(txtPay.getText());
            if (!(name.isBlank() || descr.isBlank() || type.isBlank())) {
                Job j = new Job();
                j.setName(name);
                j.setDescription(descr);
                j.setPayPerHour(pay);
                j.setType(type);
                e.getJobOfferings().add(j);
                JOptionPane.showMessageDialog(this, "Job created successfully.");
                populateTable();
            }
            else {
                JOptionPane.showMessageDialog(this, "Please input all the fields.", "Error", JOptionPane.WARNING_MESSAGE);
            }
        } catch (NumberFormatException err) {
            JOptionPane.showMessageDialog(this, "Please input a valid pay.", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCreateActionPerformed


