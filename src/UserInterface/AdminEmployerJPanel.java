    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEmployer.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblEmployer.getModel();
        Employer selectedEmployer = (Employer) model.getValueAt(selectedRowIndex, 0);
        
        m.getEmployers().getEmployers().remove(selectedEmployer);
        JOptionPane.showMessageDialog(this, "Employer deleted successfully.");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEmployer.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblEmployer.getModel();
        Employer selectedEmployer = (Employer) model.getValueAt(selectedRowIndex, 0);
        
        name.setText(selectedEmployer.getName());
        txtDescription.setText(selectedEmployer.getDescription());
        country.setText(selectedEmployer.getPerson().getCountry());
        username.setText(selectedEmployer.getPerson().getUsername());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String empname = name.getText();
        String empdescr = txtDescription.getText();
        String empcountry = country.getText();
        String empusername = username.getText();
        
        Person p =m.createPerson(empname, empcountry, empname + "@gmail.com", true, "Male", empusername + "@123", empusername, "employer");
        Employer e = new Employer();
        e.setName(empname);
        e.setDescription(empdescr);
        e.setPerson(p);
        m.getEmployers().getEmployers().add(e);
        JOptionPane.showMessageDialog(this, "Employer created successfully.");
        populateTable();
    }//GEN-LAST:event_btnCreateActionPerformed


