    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEmployer.getModel();
        model.setRowCount(0);
        for (Employer e : employers.getEmployers()) {

            model.addRow(new Object[]{
                e,
                e.getDescription(),
                e.getPerson().getCountry(),
                e.getPerson().getUsername(),
                e.getPerson().getCurrentHashedPassword(),});

        }
    }

